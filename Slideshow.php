<script>
//classes the seperate of slideshows
var slideIndex = [1,8];
var slideId = ["Slide1", "Slide2" , "Slide3" , "Slide4" , "Slide5" , "Slide6" , "Slide7" , "Slide8" , "Slide9"]

//sets the first image to show in each slideshow
show(1, 0);
show(1, 1);
show(1, 2);
show(1, 3);
show(1, 4);
show(1, 5);
show(1, 6);
show(1, 7);
show(1, 8);

//defines how to move between images by adding or subtracting 1 to the slide number
function Move(n, number) {
  show(slideIndex[number] += next, number);
}

//decides which image to show on the slideshow
function show(next, number) {
  //initialises i
  var i;
  //creates array for the selected slide and it's images
  var x = document.getElementsByClassName(slideId[number]);
  //loops slides from end to start if next is pressed on the last image
  if (next > x.length) {
    slideIndex[number] = 1
  }
  //loops slides from start to end if previous is pressed on the first image
  if (next < 2) {
    slideIndex[number] = x.length
  }
  //removes images other than the one being displayed from the page
  for (i = 0; i < x.length; i++) {
     x[i].style.display = "none";  
  }
  //displays the chosen image in the slideshow using the full image
  x[slideIndex[number]-1].style.display = "block";  
}
</script>




